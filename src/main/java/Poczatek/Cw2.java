package Poczatek;

public class Cw2 {
    public static void main(String[] args) {
        int koko = 20;
        while (koko >= 0) {
            if (koko % 2 == 0) {
                System.out.println(koko);
            }
            if (koko == 5) {
                System.out.println(koko);
            }
            koko--;
        }
    }
}
